                <p>Труба профильная - вид трубного металлопроката, представляет собой полое трубное изделие с прямоугольным, квадратным, овальным или другим сечением, толщина стенок может быть различной.</p>
                <p>Изготавливается труба из листов, чаще всего из углеродистой или низкоуглеродистой стали, путем сварки или проката на специальном стане. Чаще всего трубы идут квадратными либо прямоугольными, что дает им преимущество в прочности и способности выдерживать внушительные статические нагрузки благодаря наличию четырех ребер жесткости. Профильные трубы часто используются как замена более тяжелому и дорогостоящему металлическому брусу, швеллерам и уголкам.</p>
                <p>Когда говорят о типоразмерах трубы, то здесь имеется ввиду - высота, ширина и толщина стенки. У квадратных труб ширина и высота равны.</p>
                <p>Основные преимущества профильных труб: универсальность, сравнительно небольшая стоимость, высокая прочность, устойчивость к неблагоприятному влиянию экстремальных температур, удобная форма (упрощает погрузку/разгрузку, транспортировку и монтаж). Основной недостаток таких труб — сложности при изгибании и коррозия. Она связана с тем, что профильная труба изготавливается в основном из черных металлов. Данный недостаток можно устранить с помощью оцинковки.</p>
